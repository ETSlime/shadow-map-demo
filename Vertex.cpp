HRESULT InitVertex(void)
{
	for (int i = 0; i < MAX_COLOR; i++)
	{
		// í∏ì_ÉoÉbÉtÉ@ê∂ê¨
		D3D11_BUFFER_DESC bd;
		ZeroMemory(&bd, sizeof(bd));
		bd.Usage = D3D11_USAGE_DYNAMIC;;
		bd.ByteWidth = sizeof(VERTEX_3D) * 4 * MAX_POLYGON;
		bd.BindFlags = D3D11_BIND_VERTEX_BUFFER;
		bd.CPUAccessFlags = D3D11_CPU_ACCESS_WRITE;

		if (g_Vertex[i])
		{
			// ÉOÉçÅ[ÉoÉãÇÃí∏ì_îzóÒÇ©ÇÁí∏ì_ÉoÉbÉtÉ@ÇÇ¬Ç≠ÇÈ
			D3D11_SUBRESOURCE_DATA sd;
			ZeroMemory(&sd, sizeof(sd));
			sd.pSysMem = g_Vertex[i];

			GetDevice()->CreateBuffer(&bd, &sd, &g_VertexBuffer[i]);
		}
		else
		{
			g_VertexBuffer[i] = NULL;
		}
	}

	return S_OK;
}

//=============================================================================
// èIóπèàóù
//=============================================================================
void UninitVertex(void)
{
	for (int i = 0; i < MAX_COLOR; i++)
	{
		if (g_VertexBuffer[i])
		{
			g_VertexBuffer[i]->Release();
			g_VertexBuffer[i] = NULL;
		}
	}
}

ID3D11Buffer* GetVertexArrayBuffer(int color)
{
	return g_VertexBuffer[color];
}